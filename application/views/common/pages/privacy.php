 <h1>Confidentialité et Données Personnelles</h1>
        <p>Le présent texte illustre l'engagement de FlorBooks eu égard au respect de votre vie privée et à la protection des données personnelles vous concernant Il  peut être mis à jour pour suivre l'évolution de notre service et de vos droits. 
        FlorBooks protège la vie privée de ses utilisateurs et respecte la législation en vigueur. FlorBooks est déclaré à la CNIL (récépissé N° 71t0564709x).
        </p>

        <p class='lead' >1. Données personnelles collectées </p> 
        <p>Par données personnelles, nous entendons toutes les informations vous concernant et rattachés à votre compte fourni 
        -Données d’inscription : Nous collectons vos données d’inscription d’un compte. Ces données ne sont pas publiques.
        -Données de profil et alertes : ce sont les données que vous renseignez dans la partie profil professionnel (compétences, formation…) ces données sont facultatives, publiques et modifiables par vous sur le site.
        -Données d’utilisation :Ce sont des données générées automatiquement lorsque vous interagissez avec nos pages sous forme de cookies ou autres procédé automatique de traçage.
        </p>

        <p class='lead' >2. Utilisations des données </p>
        <p>L’utilisation des données personnelles :
        -   Les données d’inscription sont nécessaires pour créer votre compte et pour vous faire profiter des  fonctionnalités du site
        -   Les données de profil et d’alerte vous sont utiles pour vos interactions professionnelles et sociales au sein du site.
        -   Les données d’utilisation sont utilisées pour améliorer la qualité de votre consultation du site.
        </p> 

        <p> <strong>Nous n’utilisons pas votre adresse électronique ni toute autre information susceptible de vous identifier personnellement sans votre consentement </strong> </p> 
