<h2>Conseil pour les photos de compositions & bouquets</h2>

<dl>
    <dt>1. La mise en Scène</dt>
    <dd>Penser à prendre la photo de la composition florale sur un fond d'une couleur différente qui mettra mieux en valeur la création et ranger mousses seaux et autres sécateurs !</dd>

    <dt>2. Capter la lumière</dt>
    <dd>En intérieur la luminosité est moindre:Pensez à prendre vos photos au plus proche d'une source de lumière</dd>
        
    <dt>3. Eviter le flou</dt>
    <dd>En mode automatique, votre appareil va compenser la faible luminosité en augmentant le temps de prise de vue...attention au flou! Utiliser un trépied ou assurez vous de maintenir l'appareil stable</dd>
        
    <dt>4. Utiliser un flash</dt>
    <dd>Selon la portée de votre flash (en général moins de 3 m), c’est une bonne solution pour réussir conseil pour les photos de décoration florale</dd>
        
    <dt>1. Vision d’ensemble et zoom</dt>
    <dd>Le piège des photos de décorations florales est que l’œil humain voit mieux les détails. Pour un bon rendu, prenez une photo d’ensemble et quelques photos de détail.</dd>
        
    <dt>2. Des photos à chaque étape</dt>
    <dd>Si possible, faites des photos au cours de la réalisation !</dd>    

</dl>

