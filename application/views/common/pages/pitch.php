

  
<h1>florBooks en quelques mots</h1>


        <h2>L’idée :</h2>
        <p>L’idée de FlorBook est venue d’une constatation sur le terrain : Quand on exerce le métier de fleuriste, ce n’est pas facile d’exprimer son talent.<br />
        Une belle compo, c’est toujours éphémère et pas simple à faire partager. Même chose quand il s’agit de montrer un book pour un emploi ou pour un client !</p>
        <h2>L’histoire :</h2>
        <p>Tout a débuté en 2010, alors que Blanche Isabelle tentait d’enseigner quelques notions de piquage. <br />
        Après 1 heure d’efforts (et quelques mousses massacrées…) et une (magnifique) composition plus tard, la déception de savoir qu’elle tiendrait moins de 10 jours était forte. L’idée était née.</p>
        <p>Xavier en a parlé avec Raphael un ami de l’époque étudiante et Raphaël a dit Go !</p> 
        <p>Le choix des nains de jardin lui, reste inexpliqué.</p>

        <h2>Notre mission :</h2>
        <p>Notre ambition est de permettre aux talents de s’exprimer en créant la plus grande base de designs floraux.</p> 
        <p>Trouver de l’inspiration, montrez son savoir-faire ou recruter pour emploi…<br />tout ça doit être possible avec FlorBooks, la place où se retrouvent les talents !</p>


