<h1>FlorBooks en quelques mots</h1>

<br />

<p class='lead'>L’idée :</p>
<p>L’idée de FlorBooks est venue d’une constatation sur le terrain : Quand on exerce le métier de fleuriste, ce n’est pas facile d’exprimer son talent.<br />
Une belle compo, c’est toujours éphémère et pas simple à faire partager. Même chose quand il s’agit de montrer un book pour un emploi ou pour un client !</p>

<br />

<p class='lead'>Notre mission :</p>
<p>Notre ambition est de permettre aux talents de s’exprimer en créant la plus grande base de designs floraux.</p> 
<p>Trouver de l’inspiration, montrez son savoir-faire ou recruter pour emploi…tout ça doit être possible avec FlorBooks, la place où se retrouvent les talents !</p>


